public class Arithmetic {
    public static void main(String[] args) {
        int num = 100;
        int factor = 20;
        int sum = 0;
        sum = num + factor;//100+20
        System.out.println("Result adding:"+ sum);
        sum = num - factor;//100-20
        System.out.println("Result subtraction:"+ sum);
        sum = num * factor;//100*20
        System.out.println("Result multiplication:"+ sum );
        sum = num/factor;//100/20
        System.out.println("Result division:" + sum);
    }
}
