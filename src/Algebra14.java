import java.util.Scanner;

public class Algebra14 {
    public static void main(String[] args) {
        Scanner s = new Scanner(System.in);

        int sum = 0;
        System.out.println("Введи число a");
        int a = s.nextInt();
        System.out.println("Введи число b");
        int b = s.nextInt();
        sum = a + b;
        System.out.println(sum);
        sum = a * b;
        System.out.println(sum);


    }
}
